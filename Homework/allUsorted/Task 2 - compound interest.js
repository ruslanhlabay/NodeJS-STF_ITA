// Складний відсоток
// B=A*(1+P/100)^n
// B - майбутня вартість
// А = поточна вартість
// P - відсоткова ставка за розрахунковий період
// n - кількість розрахункових періодів




function CompInterest(a, p, n) {
    let b = 0

    b = a*Math.pow( (1+p/100) , n)

    return b
}


console.log (CompInterest(100, 8, 3))